
public class AnimalObjects
	{

		private String type;
		private String food;
		
		public AnimalObjects(String t, String f)
		{
			type = t;
			food = f;
		}

		public String getType()
			{
				return type;
			}

		public void setType(String type)
			{
				this.type = type;
			}

		public String getFood()
			{
				return food;
			}

		public void setFood(String food)
			{
				this.food = food;
			}
		
	}
